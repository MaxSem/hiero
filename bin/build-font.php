#!/usr/bin/php
<?php

declare(strict_types=1);

const FIRST_CHAR = 0x13000;
const LAST_CHAR = 0x1342F;

if (count($argv) !== 3) {
    bail(<<<TEXT
Syntax: {$argv[0]} <source> <destination>

Builds an SVG font for use with maxsem/hiero.
- <source> can be either a .ttf
  file (in which case extract-svgs.py will be invoked) or an intermediate
  directory with SVGs already extracted).
- <destination> is a directory.
TEXT
    );
}

$source = $rawDataDir = $argv[1];
$dest = $argv[2];

$unicodeData = loadUnicodeData();

if (is_file($source)) {
    $rawDataDir = sys_get_temp_dir() . '/' . pathinfo($source, PATHINFO_FILENAME);

    if (!is_dir($rawDataDir)) {
        mkdir($rawDataDir, recursive: true);
    }

    generateSvgFromFont($source, $rawDataDir);
}

if (!is_dir($dest)) {
    mkdir($dest, recursive: true);
}

[$characters, $boundingBox] = processSvgFiles($rawDataDir, $dest, $unicodeData['charToGardiner']);

$metadata = json_decode(file_get_contents("$rawDataDir/_font.json"), true, flags: JSON_THROW_ON_ERROR)
    or bail("Coundn't read font metadata.");

$result = [
    'metadata' => $metadata,
    'boundingBox' => $boundingBox,
    'characters' => $characters,
];

saveData("$dest/_font.php", $result);

function bail(string $message): void
{
    fputs(STDERR, "$message\n");
    exit(1);
}

function loadUnicodeData(): array
{
    try {
        return require dirname(__DIR__ ) . '/data/unicode.php';
    } catch (Throwable) {
        bail('Unicode data file not found, run bin/generate.php first.');
    }
}

function generateSvgFromFont(string $source, string $rawDataDir): void
{
    $command = 'python3 '
        . escapeshellarg(__DIR__ . '/extract-svgs.py') . ' '
        . escapeshellarg($source) . ' '
        . escapeshellarg($rawDataDir);

    echo "Executing: $command\n";

    if (passthru($command, $exitCode) === false || $exitCode !== 0) {
        bail("Error generating SVG files, code $exitCode.");
    }
}

/**
 * @param array<int, string> $charToGardiner
 * @return array<string, mixed>
 */
function processSvgFiles(string $source, string $dest, array $charToGardiner): array
{
    echo "Massaging and analysing SVG files from $source to $dest...\n";

    $result = [];
    $max = [0, 0, 0, 0];

    foreach (glob("$source/raw*.svg") as $i => $filename) {
        if ($i && $i % 100 === 0) {
            echo "  $i\n";
        }
        if (!preg_match('/(\d{5})/', $filename, $matches)) {
            continue;
        }

        $char = (int)$matches[1];
        $gardinerCode = $charToGardiner[$char] ?? null;
        if (!$gardinerCode) {
            continue;
        }

        $viewBox = processSvg($filename, "$dest/$gardinerCode.svg");
        $result[$gardinerCode] = $viewBox;
        $max[0] = min($max[0], $viewBox[0]);
        $max[1] = min($max[1], $viewBox[1]);
        $max[2] = max($max[2], $viewBox[2]);
        $max[3] = max($max[3], $viewBox[3]);
    }

    if (!$result) {
        bail("No raw*.svg files found in $source, run data/extract-svgs.py first.");
    }

    return [$result, $max];
}

function processSvg(string $source, string $dest): array
{
    $doc = new DOMDocument();
    $doc->load($source);
    $nodes = $doc->getElementsByTagName('svg');

    if ($nodes->count() === 0) {
        bail("No <svg> element found in $source");
    }
    if ($nodes->count() > 1) {
        bail("More than one svg element found in $source");
    }

    $svg = $nodes[0];
    $viewBox = $svg->getAttribute('viewBox');
    if (!preg_match('/^-?\d+ -?\d+ \d+ \d+$/', $viewBox)) {
        bail("No valid viewBox found in $source");
    }

    $result = array_map('intval', explode(' ', $viewBox));

    $svg->removeAttribute('xmlns');
    $svg->removeAttribute('xmlns:xlink');
    $svg->removeAttribute('version');

    $contents = $doc->saveXML($svg);

    if ($contents === false) {
        bail("Error serializing a transformed $source");
    }

    file_put_contents($dest, $contents) ?: bail("Error saving $source");

    return $result;
}

function saveData(string $filename, array $data): void
{
    $timestamp = (new DateTime())->format('Y-m-d H:i:s');
    $content = <<<PHP
    <?php
    // DO NOT EDIT, this is an automatically generated file.
    // Generated by data/prepare-font.php on $timestamp


    PHP;

    $content .= 'return ' . var_export($data, true) . ";\n";

    file_put_contents($filename, $content);
}
